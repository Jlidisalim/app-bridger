// Helper: search airports by city, country, or IATA code (case-insensitive, deduped)
export function searchAirports(query: string, limit = 7): Airport[] {
  if (!query.trim()) return [];
  const q = query.toLowerCase();
  const map = new Map<string, Airport>();
  for (const a of AIRPORTS) {
    const matchCity = a.city.toLowerCase().includes(q);
    const matchCountry = a.country.toLowerCase().includes(q);
    const matchIata = a.iata.toLowerCase().includes(q);
    if (matchCity || matchCountry || matchIata) {
      const key = `${a.city}|${a.country}|${a.iata}`;
      if (!map.has(key)) {
        map.set(key, a);
      }
      if (map.size >= limit) break;
    }
  }
  return Array.from(map.values());
}
